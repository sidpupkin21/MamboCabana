        function showAlerts(type, msg, position = 'body') {
            let bs_class = (type == 'success') ? 'alert-success' : 'alert-danger';
            let element = document.createElement('div');
            element.innerHTML = `
                <div class="alert-container" >
                    <div class="alert ${bs_class} alert-dismissible fade show" role="alert">
                    <strong class="me-3">${msg}</strong>
                    <button type="button" class="btn-close" data-bs-dismiss="alert" aria-label="Close"></button>
                    </div>
                </div>
                `;

            if (position == 'body') {
                document.body.append(element);
                element.classList.add('custom-alert');
            } else {
                document.getElementById(position).appendChild(element);
            }
            setTimeout(remAlert, 5000);
        }

        function remAlert() {
            var alertElement = document.getElementsByClassName('alert')[0];
            if (alertElement) {
                alertElement.remove();
            }
        }


        function setActive() {
            let navbar = document.getElementById('profile-header');
            let a_tags = navbar.getElementsByTagName('a');

            for (i = 0; i < a_tags.length; i++) {
                let file = a_tags[i].href.split('/').pop();
                let file_name = file.split('.')[0];

                if (document.location.href.indexOf(file_name) >= 0) {
                    a_tags[i].classList.add('active');
                }

            }
        }

        let info_form = document.getElementById('info-form');

        info_form.addEventListener('submit', function(e) {
            e.preventDefault();

            let data = new FormData();
            data.append('info_form', '');
            data.append('name', info_form.elements['name'].value);
            data.append('username', info_form.elements['username'].value);
            data.append('dob', info_form.elements['dob'].value);
            data.append('phone', info_form.elements['phone'].value);
            data.append('country', info_form.elements['country'].value);

            let xhr = new XMLHttpRequest();
            xhr.open("POST", "logic/profile.php", true);

            xhr.onload = function() {
                //console.log(this.responseText);
                if (this.responseText == 3){//"user_exists") {
                    showAlerts('error', "This username is already registered, Please try another one");
                    info_form.reset();
                } else if (this.responseText == 2){//"phone_exists") {
                    showAlerts('error', "this phone number is already registered, Please try another one");
                    info_form.reset();
                } else if (this.responseText == 0) {
                    showAlerts('error', "No changes have been made");
                } else if(this.responseText == 1){
                    // console.log(this.responseText);
                    showAlerts('success', 'Your changes have been saved');
                }
            }
            xhr.send(data);
        });

